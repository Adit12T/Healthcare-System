package Inventory;

public class StockDetailBean {
	int itemid;
	String itemname;
	int suppiled;
	int issue;
	int total;
	
	public int getItemid() {
		return itemid;
	}
	public void setItemid(int itemid) {
		this.itemid = itemid;
	}
	public String getItemname() {
		return itemname;
	}
	public void setItemname(String itemname) {
		this.itemname = itemname;
	}
	public int getSuppiled() {
		return suppiled;
	}
	public void setSuppiled(int suppiled) {
		this.suppiled = suppiled;
	}
	public int getIssue() {
		return issue;
	}
	public void setIssue(int issue) {
		this.issue = issue;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	

}
